# Example of combining lists

list_a = [3, 'go', 7.1]
list_b = [4, 'hi', 1]
list_c = list_a + list_b
print(len(list_c))
print(list_c)

# Similarly, you can use the multiplication symbol * to extend a list by repetition. This is useful for creating a new list of a desired length.

my_phone_number = [9, 6, 7,] + [1] * 4
print(my_phone_number)
new_empty_list = [0] * 20
print(new_empty_list)
